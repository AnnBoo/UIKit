import UIStyle from './index';

export default class UIStyleBorder {
    // borders
    static common() {
        return UIStyle.border;
    }

    static bottom() {
        return UIStyle.bottom;
    }

    static bottomAction() {
        return UIStyle.borderBottomAction;
    }

    static left() {
        return UIStyle.borderLeft;
    }

    static top() {
        return UIStyle.borderTop;
    }
}
